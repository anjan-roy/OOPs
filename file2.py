#static method code
class calculator:
    @staticmethod
    def sum(a,b):
        print(a+b)
    @staticmethod
    def sub(a, b):
        print(a - b)

    @staticmethod
    def mul(a, b):
        print(a * b)

    @staticmethod
    def div(a, b):
        print(a / b)
calculator.sum(23,46)
calculator.sub(45,21)
calculator.mul(23,89)
calculator.div(33,3)